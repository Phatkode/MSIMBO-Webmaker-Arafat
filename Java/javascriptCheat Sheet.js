// 				(*fun) 	 =   Command (in blue)
//				(*nm)    =   Name of Function (in green)
// 				(*num)   =   Number to run function on
// 				(*oa)    =   Starting Answer
// 				(*i)     =   Starting variable 
// 				(*na)    =   New answer
// 				(*pna)   =   Previous new answer
// 				(*cr)    =   # Console result
