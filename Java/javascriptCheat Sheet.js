//TERMS ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// 				(*fun) 	 =   Command (in blue)
//				(*nm)    =   Name of Function (in green)
// 				(*num)   =   Number to run function on
// 				(*oa)    =   Starting Answer
// 				(*i)     =   Starting variable 
// 				(*na)    =   New answer
// 				(*pna)   =   Previous new answer
// 				(*cr)    =   # Console result
//EXPLANATION~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// *oa is starting answer
// on first loop add *oa to *i to get *na
// Rule: *na # becomes *pna # on next line
//EQUATION ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
//*First Loop 				(*oa)  +  (*i)   =  (*na)  
//Other loops 				(*pna) +  (i+2)  =  (*na)  
//LOOP SAMPLE for (num10)~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// Header| 		       		(o/pna)+  (i+2)  =  (*na)		check *fun| [num]   (*cr)   Does....
// Loop 1| 		       	    (0)    +  (1)    =  (1)         check *fun| [2]     1       0+1=1
// Loop 2| 		       		(1)    +  (1+2)  =  (3)         check *fun| [3-5]   4       0+1+3=4
// Loop 3| 		       		(3)    +  (3+2)  =  (5)         check *fun| [6-8]   9       0+1+3+5=9
// Loop 4| 		       		(5)    +  (5+2)  =  (7)         check *fun| [9]     16      0+1+3+5+7=16
// Loop 5| 		       		(5)    +  (7+2)  =  (9)         check *fun| [10]    25      0+1+3+5+7+9=25


